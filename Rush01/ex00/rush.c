#include <stdio.h>

void	rush(int **frame)
{
    int i;
    int j;
    
    i = 0;
	while (i < 4) {
        j = 0;
        while (j < 4) {
            printf("%d ", frame[i][j]);
            j++;
        }
        printf("\n");
        i++;
    }
}